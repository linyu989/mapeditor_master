def map_cells_get(dict_list):
    # 实例化cell类
    cell = map.Cell()

    cell_code = dict_list['cell_code']
    location_x = dict_list['location_x']
    location_y = dict_list['location_y']
    cell_type = dict_list['cell_type']
    cell_size_width = dict_list['width']
    cell_size_length = dict_list['length']

    # 得到map_shape
    row = len(set(location_y))
    col = len(set(location_x))
    # 长宽对应列行
    map_shape = [col, row]
    # 实例化cells类
    map_cells = map.MapCells(map_shape)
    # unitsize大小
    sizew = (min(cell_size_width)) * 1000
    sizel = (min(cell_size_length)) * 1000
    sizeunit = [sizel, sizew]

    minx = (min(location_x))
    miny = (min(location_y))
    edge = 100

    # # 最小坐标置零后的maxsize
    maxsizew = max(location_y) - miny
    maxsizel = max(location_x) - minx
    # maxsize得到最大坐标值，实际渲染时从该坐标起点进行色块填充
    # 故maxsize需要加上最大色块长宽，可添加一定余量
    maxsizew = maxsizew + (max(cell_size_width)) * 1000 + edge
    maxsizel = maxsizel + (max(cell_size_length)) * 1000 + edge

    max_cell_size = [int(maxsizel), int(maxsizew)]
    map_cells.max_cell_size = max_cell_size

    print(f'maxsize is {max_cell_size}')

    locations = []
    location_x_minzero = []
    location_y_minzero = []
    minx = (min(location_x))
    miny = (min(location_y))
    for i in range(len(location_x)):
        # 不从零开始留边
        zerox = location_x[i] - minx
        zeroy = location_y[i] - miny
        location_x_minzero.append(zerox)
        location_y_minzero.append(zeroy)
        locations.append([zerox, zeroy])

    delta = 0
    # width列表存储每列对应元素个数
    width_count = []

    for i in range(col):
        x = locations[delta][0]
        # 该列元素个数
        count = location_x_minzero.count(x)
        # 存储至列表
        width_count.append(count)

        for j in range(count):
            index = [i, j]
            cellcode = cell_code[delta]
            type = cell_type[delta]
            sizew = (cell_size_width[delta]) * 1000
            sizel = (cell_size_length[delta]) * 1000
            size = [sizel, sizew]
            corner = locations[delta]
            map_cells.setcell(type=type, index=index, corner=corner, size=size, cellnode=cellcode)

            delta += 1

    return map_cells, width_count


