# f为空白图，l为坐标点，s为对应色块，v为颜色
def render(f, l, s, v):
    # fig即maxsize已经除以10，这里将corner与size也除以10统一处理
    l = [int(l[0] / 10), int(l[1] / 10)]
    s = [int(s[0] / 10), int(s[1] / 10)]
    # size长宽调节边界避免格子相连
    dl = int(s[0] / 5)
    dw = int(s[1] / 5)
    # 循环作用将corner坐标点区域填充
    for i in range(l[0], l[0] + s[0]):
        for j in range(l[1], l[1] + s[1]):
            # fig = [red, green, blue]三个数组合并的
            f[0][i, j] = v[0]
            f[1][i, j] = v[1]
            f[2][i, j] = v[2]
            if i >= l[0] + s[0] - dl:
                f[0][i, j], f[1][i, j], f[2][i, j] = 248, 250, 250
            if j >= l[1] + s[1] - dw:
                f[0][i, j], f[1][i, j], f[2][i, j] = 248, 250, 250


